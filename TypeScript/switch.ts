var grade:string="A";
switch(grade){
    case "A":{
        console.log("Excellent");
        break;
    }
    case "B":{
        console.log("Good");

    }
    case "C":{
        console.log("Fair");

    }
    case "D":{
        console.log("Poor");

    }
    default:{
        console.log("Invalid choice");

    }
}