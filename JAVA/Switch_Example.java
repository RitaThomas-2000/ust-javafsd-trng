package com.ust;

public class Switch_Example {
	public static void main(String args[]) {
	String str="Red";
	
	switch(str) {
	case "Red": 
		System.out.println("I am in Red");
		System.out.println("*******");
		break;
	default:
		System.out.println("in default");
		break; 
	case "Green":
		System.out.println("I am in Green");
		System.out.println("*******");
		break;
	case "Yellow":
		System.out.println("I am in Yellow");
		System.out.println("*******");
		break;
	
		

		
		
		
	}
}
}
